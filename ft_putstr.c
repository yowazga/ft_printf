#include "ft_printf.h"

void	ft_putstr(char *str, int *count)
{
	int	i;

	i = 0;
	if (str)
	{
		while (str[i])
		{
			write(1, &str[i], 1);
			i++;
			*count += 1;
		}
	}
	else
	{
		write(1, "(null)", 6);
		*count += 6;
	}
}
